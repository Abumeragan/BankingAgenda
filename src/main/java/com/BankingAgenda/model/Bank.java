package com.BankingAgenda.model;

public class Bank {
	
	private long idBank;
	private String name;
	
	public Bank() {
		
	}

	public long getIdBank() {
		return idBank;
	}

	public void setIdBank(long idBank) {
		this.idBank = idBank;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
